#While loops:
#while loops execute statements while the condition is True. As soon as the condition becomes False, the interpreter comes out of the while loop.
count = 5
while (count > 6):
  print(count)
  count = 6

#Else if Loops:
x = 5
while (x > 0):
  print(x)
  x = x - 1
else:
  print('counter is 0')

